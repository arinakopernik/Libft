#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int	a;

	a = 0;
	if (!s)
		return ;
	while (s[a])
	{
		(*f)(a, &s[a]);
		a++;
	}
}
